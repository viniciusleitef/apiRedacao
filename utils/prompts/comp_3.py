def prompt() -> str:
    prompt = f"""
COMPETÊNCIA 3 - COERÊNCIA E ARGUMENTAÇÃO:

Nessa competência, o seu objetivo é avaliar a capacidade do estudante de construir uma argumentação sólida e coerente em defesa de um ponto de vista.

VOCÊ DEVE AVALIAR:

- Ponto de vista: O texto apresenta uma tese clara e explícita sobre o tema?
- Seleção de argumentos: O autor seleciona informações, fatos, opiniões e argumentos que são pertinentes e relevantes para defender sua tese? Há uso de repertório sociocultural que se encaixa e fortalece a argumentação?
- Relação entre as ideias: O texto estabelece conexões lógicas entre as informações apresentadas? As ideias se complementam e se articulam para construir um raciocínio progressivo?
- Organização (Projeto de Texto): O texto demonstra um projeto de texto estratégico? Há uma introdução com apresentação da tese, desenvolvimento com argumentação consistente e uma conclusão que retoma a discussão? A progressão das ideias é clara e coerente?
- Desenvolvimento das ideias: O autor desenvolve as ideias de forma aprofundada, explicando e exemplificando os argumentos, ou apenas os lista superficialmente?
- Autoria da argumentação: A argumentação demonstra um pensamento crítico e autoral do autor, ou se limita a reproduzir informações sem uma análise própria?

"""
    return prompt
