def prompt() -> str:
    prompt = f"""
COMPETÊNCIA 4 - COESÃO TEXTUAL:
INSTRUÇÕES:
- Avalie o uso de mecanismos linguísticos na construção de argumentos
- Verifique a presença e adequação dos elementos coesivos
- Analise o uso de conjunções, pronomes e técnicas de coesão lexical
- Avalie a variedade e adequação dos elementos coesivos

CONTEXTO:
- Tipos de Conectivos:
    - Conectivos Intraparágrafos: Usados dentro do mesmo parágrafo para conectar orações e períodos, organizando as ideias internas. Principais Exemplos: "além disso", "dessa forma", "porém", "assim como", "ou seja", "contudo", "visto que". Obviamente, existem muitos outros, não se esqueça disso.
    - Conectivos Interparágrafos: Utilizados para criar uma ponte entre um parágrafo e outro, mostrando a relação de sentido entre eles e garantindo a progressão textual. Principais Exemplos: "Diante desse cenário", "Nesse sentido", "Ademais", "Em primeiro lugar", "Outrossim", "Portanto", "Em suma". Obviamente, existem muitos outros, não se esqueça disso.
- Inadequações:
    - Ocorrem quando os conectivos são usados de forma incorreta, causando incoerência ou ambiguidade no texto.
    - Exemplo: 
        Erro: "Ele faltou à prova, porque ficou com nota alta."
        Correto: "Ele faltou à prova, mas ficou com nota alta." 
        Explicação: "Porque" indica causa, mas a frase expressa surpresa ou oposição.
    Essas inadequações atrapalham a lógica do texto e a clareza da mensagem.
"""
    return prompt
  
